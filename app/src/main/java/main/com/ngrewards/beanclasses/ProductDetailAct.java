package main.com.ngrewards.beanclasses;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.List;

/**
 * Created by technorizen on 28/11/18.
 */

public class ProductDetailAct {
    @SerializedName("id")
    @Expose
    private String id;
    @SerializedName("product_name")
    @Expose
    private String productName;
    @SerializedName("product_description")
    @Expose
    private String productDescription;
    @SerializedName("price")
    @Expose
    private String price;
    @SerializedName("merchant_detail")
    @Expose
    private List<MerchantListBean> merchantDetail = null;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductDescription() {
        return productDescription;
    }

    public void setProductDescription(String productDescription) {
        this.productDescription = productDescription;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public List<MerchantListBean> getMerchantDetail() {
        return merchantDetail;
    }

    public void setMerchantDetail(List<MerchantListBean> merchantDetail) {
        this.merchantDetail = merchantDetail;
    }
}
