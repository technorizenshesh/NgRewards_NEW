package main.com.ngrewards.Models;

public class MenuModal {


    private String name;
    private String title;
    private String titleDiscription;
    private String price;
    private String id;
    private String list;
    private String image;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {

        this.title = title;
    }

    public String getTitleDiscription() {
        return titleDiscription;
    }

    public void setTitleDiscription(String titleDiscription) {

        this.titleDiscription = titleDiscription;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {

        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {


        this.id = id;
    }

    public String getList() {
        return list;
    }

    public void setList(String list) {

        this.list = list;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {

        this.image = image;
    }
}
