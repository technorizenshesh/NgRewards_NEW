package main.com.ngrewards.Models;

public class ModalMenuList {


    private String name;
    private String title;
    private String titleDiscription;
    private String price;
    private String id;
    private String list;
    private String image;
    private String item_id;
    private String status;
    private String menu_id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {

        this.title = title;
    }

    public String getTitleDiscription() {
        return titleDiscription;
    }

    public void setTitleDiscription(String titleDiscription) {

        this.titleDiscription = titleDiscription;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {

        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {


        this.id = id;
    }

    public String getList() {
        return list;
    }

    public void setList(String list) {

        this.list = list;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {

        this.image = image;
    }

    public String getItem_id() {
        return item_id;
    }

    public void setItem_id(String item_id) {

        this.item_id = item_id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {

        this.status = status;
    }

    public String getMenu_id() {
        return menu_id;
    }

    public void setMenu_id(String menu_id) {

        this.menu_id = menu_id;
    }
}
